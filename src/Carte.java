public class Carte {
    char caracter;
    char couleur;
    boolean estJoker;
    String codeTrophee;

    public Carte(char caracter, char couleur, boolean estJoker, String codeTrophee) {
        this.caracter = caracter;
        this.couleur = couleur;
        this.estJoker = estJoker;
        this.codeTrophee = codeTrophee;
    }
}
